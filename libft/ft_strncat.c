#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	char *addr;

	addr = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0' && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	*dest = '\0';
	return (addr);
}
