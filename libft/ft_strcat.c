#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char *addr;

	addr = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (addr);
}
